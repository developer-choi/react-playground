import type {Metadata, Viewport} from 'next';
import {getFontByLanguage} from '@/utils/service/common/i18n/font';
import '@/styles/global.scss';
import '@forworkchoe/core/dist/core.css';
import {LayoutProps} from '@/types/declaration/next';

// https://docs.google.com/document/d/16UpoOvdENYDO3fHK-YyR70Vet3gyjFbgxT-fwi4R3cY/edit
export const metadata: Metadata = {
  title: 'Create Next App',
  description: 'Generated by create next app',
  icons: {
    icon: [
      {
        url: `/imgs/favicon.ico`, // 원래 이자리에 사이즈별 아이콘 파일명이 들어가야함.
        sizes: `32x32`,
      },
    ],
    apple: [72, 96, 144].map((size) => ({
      url: `/imgs/favicon.ico`, // 원래 이자리에 사이즈별 아이콘 파일명이 들어가야함.
      sizes: `${size}x${size}`,
    })),
  },
};

export const viewport: Viewport = {
  themeColor: 'white',
};

export default async function RootLayout({children}: LayoutProps) {
  const font = await getFontByLanguage();

  return (
    <html lang="en">
    <body className={font.className}>{children}</body>
    </html>
  );
}
